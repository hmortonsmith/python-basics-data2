

# Logical Operators: equate to something
num_a = 10
num_b = 10
num_c = 13

# Greater than
print(num_b > num_c)
# Smaller than
print(num_b < num_c)
# Greater than or equal
print(num_b >= num_a)
# Smaller than or equal
print(num_b <= num_c)
# Equate
print(num_a == num_b)
print( num_a == num_c)

# Logical NOT
print( num_a != num_c)

# Boolean - True or False - Class of it's own
print(True)
print(type(True))
print(False)
print(type(False))
